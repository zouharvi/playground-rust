pub fn gcd(mut a: i32, mut b: i32) -> i32 {
    while b != 0 {
        let t = a % b;
        a = b;
        b = t;
    }
    a
}

pub fn subarray_gcd(nums: Vec<i32>, k: i32) -> i32 {
    let mut count: i32 = 0;
    // check every subarray
    for a_i in 0..nums.len() {
        // rolling update of gcd
        let mut prev_gcd = nums[a_i];
        for b_i in (a_i)..nums.len() {
            // speed up special case of subarrays of length 1
            prev_gcd = if a_i == b_i {
                nums[a_i]
            } else {
                gcd(prev_gcd, nums[b_i])
            };
            if prev_gcd == k {
                count += 1;
            }
        }
    }
    count
}

